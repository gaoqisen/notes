<h4 id="主语" tabindex="-1"><a class="header-anchor" href="#主语" aria-hidden="true">#</a> 主语</h4>
<ul>
<li>
<p>位置：句子开头</p>
</li>
<li>
<p>充当者：名词、代词、数词、短语。</p>
</li>
<li>
<p>描述：需要描述的事物或者对象。</p>
</li>
<li>
<p>例子：I^主语 will drink water.  // 我要喝水的“我”就是主语</p>
</li>
</ul>
<h4 id="谓语" tabindex="-1"><a class="header-anchor" href="#谓语" aria-hidden="true">#</a> 谓语</h4>
<ul>
<li>
<p>位置：主语之后</p>
</li>
<li>
<p>描述：描述表示主语的动作或状态。</p>
</li>
<li>
<p>充当者：be动词（am,is,are）、行为动词（跑、动等实际动作意义的词）</p>
</li>
<li>
<p>例子：I will^谓语 drink water.  // 我要喝水的“要”就是谓语</p>
</li>
</ul>
