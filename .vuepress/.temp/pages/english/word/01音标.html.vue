<h3 id="_1-1-元音" tabindex="-1"><a class="header-anchor" href="#_1-1-元音" aria-hidden="true">#</a> 1.1 元音</h3>
<p>是产生于声带振动时，口腔呈现较开放状态，通气无阻碍的音素。在英语中，元音通常由不同的口腔形状产生，而且它们通常是单独构成一个音节的核心。字母除了辅音之外还有另一个读法的音标。</p>
<p>A , E , I , O , U</p>
<h3 id="_1-2-辅音" tabindex="-1"><a class="header-anchor" href="#_1-2-辅音" aria-hidden="true">#</a> 1.2 辅音</h3>
<p>就是通过辅助的发音，就可以是音标。是产生于口腔、鼻腔或喉腔中有阻碍或摩擦的音素。在英语中，辅音通常涉及到不同的口腔位置和塞气音的产生</p>
<p>如：B [bi:]，读的时候就是b和i连在一起读就是B了，单独的b就是B的音标读法。</p>
