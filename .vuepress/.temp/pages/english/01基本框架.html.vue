</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="_2、主语-系动词-表语" tabindex="-1"><a class="header-anchor" href="#_2、主语-系动词-表语" aria-hidden="true">#</a> 2、主语 ＋ 系动词 ＋ 表语</h4>
<ul>
<li>特点：句子谓语动词都不能表达一个完整的意思，必须加上一个表明主语身份或状态的表语构成复合谓语，才能表达完整的意思。</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>This | is | an English-Chinese dictionary;  //这是本英汉词典。

