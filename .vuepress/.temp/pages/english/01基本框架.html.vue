<h3 id="_1-2-词性详细" tabindex="-1"><a class="header-anchor" href="#_1-2-词性详细" aria-hidden="true">#</a> 1.2 词性详细</h3>
<h4 id="_1、名词-noun-n" tabindex="-1"><a class="header-anchor" href="#_1、名词-noun-n" aria-hidden="true">#</a> 1、名词(noun,n)</h4>
<p>描述：表示人或者事物的名称</p>
<p>分类：</p>
<ol>
<li>
<p>可数名词(countable noun,c): 有单数变复数的变化[plural,pl]，apple -- apples)</p>
</li>
<li>
<p>不可数名词(uncountable noun,u): 没有变化形式。</p>
</li>
</ol>
<p>常见:</p>
