<h4 id="_3、形容词-adjective-adj" tabindex="-1"><a class="header-anchor" href="#_3、形容词-adjective-adj" aria-hidden="true">#</a> 3、形容词(adjective,adj)</h4>
<p>描述：用来修饰名词、表示人或事物的特征</p>
<p>位置：放在名词前面修饰名词</p>
<p>常见：little 小的，fat 肥胖的，thin 瘦的，big 大的</p>
<h4 id="_4、副词-adverb-adv" tabindex="-1"><a class="header-anchor" href="#_4、副词-adverb-adv" aria-hidden="true">#</a> 4、副词(adverb,adv)</h4>
<p>描述：表示行为或状态特征的词</p>
<p>位置：修饰动词或者形容词</p>
<h4 id="_5、动词-verb-v" tabindex="-1"><a class="header-anchor" href="#_5、动词-verb-v" aria-hidden="true">#</a> 5、动词(verb,v)</h4>
<p>描述：表示动作或者状态</p>
<p>位置：动作或者状态的词</p>
<p>常见：touch 触摸、look 看、know 认识</p>
<h4 id="_6、数词-numeral-num" tabindex="-1"><a class="header-anchor" href="#_6、数词-numeral-num" aria-hidden="true">#</a> 6、数词(numeral,num)</h4>
<p>描述：表示数量或顺序的词语</p>
<h4 id="_7、冠词-article-art" tabindex="-1"><a class="header-anchor" href="#_7、冠词-article-art" aria-hidden="true">#</a> 7、冠词(article,art)</h4>
<p>描述：帮忙说明名词名词所指的范围</p>
<p>位置：用在名词前</p>
<p>不定冠词: an  a    一、一个</p>
<p>定冠词: the      这、这个</p>
<p>中文样式：我今天买了一支<code v-pre>不定冠词</code> 笔，这^定冠词 支笔很好看</p>
<h4 id="_8、介词-preposition-prep" tabindex="-1"><a class="header-anchor" href="#_8、介词-preposition-prep" aria-hidden="true">#</a> 8、介词(preposition, prep)</h4>
<p>位置：用在名词或者代词前。</p>
<p>描述：说明它与别的词之间的关系，时间介词，方位介词</p>
<p>常见： in 在...离,on 在 ... 上,down,up,under 在 ... 之下，at 在</p>
<h4 id="_9、连词-conjunction-conj" tabindex="-1"><a class="header-anchor" href="#_9、连词-conjunction-conj" aria-hidden="true">#</a> 9、连词(conjunction,conj)</h4>
<p>描述：逻辑关系词,表示人或事物的名称。</p>
<p>常见：if,because,but</p>
<h4 id="_10、感叹词-interjection-int" tabindex="-1"><a class="header-anchor" href="#_10、感叹词-interjection-int" aria-hidden="true">#</a> 10、感叹词(interjection,int)</h4>
<p>描述：语气词，代替代替名词，数词，形容词等。</p>
<p>常见：oh,hello,hi,yeah</p>
