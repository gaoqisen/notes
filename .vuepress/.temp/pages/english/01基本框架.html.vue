</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="_3、主语-谓语-宾语" tabindex="-1"><a class="header-anchor" href="#_3、主语-谓语-宾语" aria-hidden="true">#</a> 3、主语 ＋ 谓语 ＋ 宾语</h4>
<ul>
<li>谓语动词都具有实义，都是主语产生的动作，但不能表达完整的意思，必须跟一个宾语，即动作的承受者，才能使意思完整。</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>Who ｜ knows ｜ the answer?  // 谁知道答案 

