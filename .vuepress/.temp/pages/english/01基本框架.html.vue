</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="_4、主语-谓语-间宾-直宾" tabindex="-1"><a class="header-anchor" href="#_4、主语-谓语-间宾-直宾" aria-hidden="true">#</a> 4、主语 ＋ 谓语 ＋ 间宾 ＋直宾</h4>
<ul>
<li>有些及物动词有两个宾语。这两个宾语通常一个指人，为间接宾语。一个指物，为直接宾语。间接宾语一般位于直接宾语之前。一般顺序：动词 ＋ 间接宾语 ＋ 直接宾语</li>
</ul>
