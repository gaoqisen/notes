<h4 id="主语-谓语" tabindex="-1"><a class="header-anchor" href="#主语-谓语" aria-hidden="true">#</a> 主语＋谓语：</h4>
<ul>
<li>特点：句子的谓语动词都能表达完整的意思。这类动词叫做不及物动词，后面可以跟副词、介词短语、状语从句等。</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>The moon ｜ rose.  // 月亮升起

The sun ｜ was shining.  // 太阳照耀

The pen | writes smoothly.  // 这支笔书写流利
  
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="主语-系动词-表语" tabindex="-1"><a class="header-anchor" href="#主语-系动词-表语" aria-hidden="true">#</a> 主语 ＋ 系动词 ＋ 表语</h4>
<ul>
<li>特点：句子谓语动词都不能表达一个完整的意思，必须加上一个表明主语身份或状态的表语构成复合谓语，才能表达完整的意思。</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>This | is | an English-Chinese dictionary;  //这是本英汉词典。

He | is growing | tall and strong;  // 他长的又高又壮
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="主语-谓语-宾语" tabindex="-1"><a class="header-anchor" href="#主语-谓语-宾语" aria-hidden="true">#</a> 主语 ＋ 谓语 ＋ 宾语</h4>
<ul>
<li>谓语动词都具有实义，都是主语产生的动作，但不能表达完整的意思，必须跟一个宾语，即动作的承受者，才能使意思完整。</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>Who ｜ knows ｜ the answer?  // 谁知道答案 

He | enjoys | reading;  // 他喜欢看书

I | want | to have a cup of tea;  // 我想喝杯茶
 
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="主语-谓语-间宾-直宾" tabindex="-1"><a class="header-anchor" href="#主语-谓语-间宾-直宾" aria-hidden="true">#</a> 主语 ＋ 谓语 ＋ 间宾 ＋直宾</h4>
<ul>
<li>有些及物动词有两个宾语。这两个宾语通常一个指人，为间接宾语。一个指物，为直接宾语。间接宾语一般位于直接宾语之前。一般顺序：动词 ＋ 间接宾语 ＋ 直接宾语</li>
</ul>
<div class="language-text line-numbers-mode" data-ext="text"><pre v-pre class="language-text"><code>I | gave | my car | a wash. // 我洗了我的车

He | showed | me | how to run the machine.  // 他教我开机器   

</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><h4 id="主语-谓语-宾语-补语" tabindex="-1"><a class="header-anchor" href="#主语-谓语-宾语-补语" aria-hidden="true">#</a> 主语 ＋ 谓语 ＋ 宾语 ＋ 补语</h4>
<ul>
<li>动词虽然是及物动词，但是只跟一个宾语还不能表达完整的意思，必须加一个补充成份来补足宾语，才能使意思完整。</li>
</ul>
