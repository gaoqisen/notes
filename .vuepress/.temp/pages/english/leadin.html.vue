<h2 id="名词" tabindex="-1"><a class="header-anchor" href="#名词" aria-hidden="true">#</a> 名词</h2>
<blockquote>
<p>可数名词才有单复数变化，不可数名词一般只有单数形式，没有复数形式。物质名词和抽象名词借助单位词表示一定的数量,如：a cup of tea, a loaf of bread。 有些物质和抽象名词变为复数后意义就不一样了，如：air &gt; airs （神气）, good &gt; goods (商品), time &gt; times (时代）等。</p>
</blockquote>
