<h5 id="百度的动词变换方式-昨天干了什么-一般过去时-动词-ed" tabindex="-1"><a class="header-anchor" href="#百度的动词变换方式-昨天干了什么-一般过去时-动词-ed" aria-hidden="true">#</a> 百度的动词变换方式（昨天干了什么......一般过去时: 动词＋ed）</h5>
<ul>
<li>
<p>直接加ed：work—— worked look——looked</p>
</li>
<li>
<p>以不发音e结尾的单词，直接加d：live ——lived hope——hoped use——used</p>
</li>
<li>
<p>以辅音字母+y结尾的，变y为i加ed：study—— studied carry——carried worry——worried</p>
</li>
<li>
<p>以重读闭音节结尾的，双写最后的辅音字母+ed：stop—— stopped plan——planned
重读闭音节体现形式为辅-元-辅结构，例如nod,n为辅音，o为元音，d为辅音。</p>
</li>
<li>
<p>以ic结尾的动词，要把ic变成ick再加ed，如picnic→picnicked，traffic→trafficked</p>
</li>
<li>
<p>不规则变化的动词过去式：
have---had are---were get---got say---said feel---felt do/does---did is---was go---went
drink--drank eat--ate bring----brought think----thought buy----bought catch---- caught teach ---- taught sit----sat wear----wore cut----cut sweep----swept sleep——slept become----became</p>
</li>
<li>
<p>以辅元辅结尾的加d</p>
</li>
</ul>
<p>明天干什么......一般将来时:  will + 动词</p>
