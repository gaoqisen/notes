<h3 id="七层详解" tabindex="-1"><a class="header-anchor" href="#七层详解" aria-hidden="true">#</a> 七层详解</h3>
<ul>
<li>应用层（针对特定应用的协议，参考模型的最底层）
<ul>
<li>应用层该协议在所需要传送的数据的前端附加一个首部信息。该首部信息里面会标明了内容和收件人。而当接受方接收到该数据后，会分析数据首部和数据正文，最终实现邮件传输。</li>
</ul>
</li>
<li>表示层（设备固有数据格式和网络标准数据格式的转换）
<ul>
<li>表示层的作用是把数据从某个计算机特定的数据格式转换为网络通用的标准数据格式，同时可以反转过来。其目的就是为了统一网络数据格式。即便是一段简单的文字也有着众多的编码格式，例如：UTF-8、UTF-16和EUC-JP等等。而表示层之间为了能够识别编码格式也会在数据前端附加首部信息。</li>
</ul>
</li>
<li>会话层（通信之间的管理）
<ul>
<li>会话层主要负责决定使用何种连接方式，例如在发送邮件的时候可以选择每一封发送完成后重新建立连接发送，也可以选择一个连接同时发送数个邮件，甚至可以同时建立数个连接发送。会话层也会在数据前端记录数据传送顺序的信息。（会话层负责决定建立连接和断开连接的十几，而传输层才是进行实际的建立和断开处理）。</li>
</ul>
</li>
<li>传输层（管理节点之间的数据传输）
<ul>
<li>传输层主要负责是建立连接和断开连接，并确保信息是否达到目标地址，它会在通信两端的计算机进行确认，如果数据没有达到，它会进行重发。为了确保可靠性，也会在数据前端加上首部信息以识别这一分层的数据。</li>
</ul>
</li>
<li>网络层（网络地址和路由的选择）
<ul>
<li>网络层中主要负责的是基于目的地址，在众多的数据链路中，将数据从发送端发送到接收端。网络层中会把目的地址的相关信息放在首部发送到下一层。</li>
</ul>
</li>
<li>数据链路层（互连设备之间传送和识别数据帧）
<ul>
<li>数据的传输实际还是通过物理的传输方式，而数据链路层实际就是在互连的设备之间进行数据处理。</li>
</ul>
</li>
<li>物理层（界定连接器和网线的规格）
<ul>
<li>物理层则是将0，1的数据转为电信号或者光信号给物理传输介质，而连接这些物理传输介质的设备之间是通过MAC地址（物理地址或硬件地址）传输。因此，物理层会把MAC地址作为首部信息放在数据前端，将其发送到网络。</li>
</ul>
</li>
</ul>
