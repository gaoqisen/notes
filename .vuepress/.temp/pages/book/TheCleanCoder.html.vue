<h4 id="_1-1清楚你要什么" tabindex="-1"><a class="header-anchor" href="#_1-1清楚你要什么" aria-hidden="true">#</a> 1.1清楚你要什么</h4>
<p>清楚自己负责的是什么，并为自己收拾残局</p>
<h4 id="_1-2-担当责任" tabindex="-1"><a class="header-anchor" href="#_1-2-担当责任" aria-hidden="true">#</a> 1.2 担当责任</h4>
<p>自己写的代码要对它负责，如果有bug需要自己去负责，不要推卸责任。并且需要想清楚为什么会有这个bug，现在如何更好的解决这个bug，以及以后如何防止相同的事情发生</p>
<h4 id="_1-3-职业道德" tabindex="-1"><a class="header-anchor" href="#_1-3-职业道德" aria-hidden="true">#</a> 1.3  职业道德</h4>
<ol>
<li>需要了解自己的领域：必须精通设计模式、设计原则、方法（结构化分析、瀑布模型等）、实践（测试驱动开发、面向对象设计、持续集成）、工件（UML图、结构图）。</li>
<li>坚持学习：与时俱进，学习不同的语言</li>
<li>练习：歌手练习声音、医生联系手术缝合、音乐家练习音阶</li>
