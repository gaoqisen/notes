<li>辅导：辅导新人的同时自己也会受益</li>
<li>了解业务领域：如果不了解业务是写不出好的程序的，需要花时间去了解业务</li>
<li>与顾客保持一致：雇主的问题就是你的问题。必须弄清楚问题，并找到最佳解决方案。</li>
<li>谦虚：专业人士都知道自己也会摔跟头，不要因别人犯错了就对之横加贬损，因为自己很可能就是下一个犯错的人。如果遇到挫折最好的办法就是一笑而过，并在上面长个心眼</li>
</ol>
<h3 id="第二章-说-不" tabindex="-1"><a class="header-anchor" href="#第二章-说-不" aria-hidden="true">#</a> 第二章 说“不”</h3>
<p>能就是能，不能就是不能。不要说“试试看”。-- 尤达</p>
<h4 id="_2-1-对抗角色" tabindex="-1"><a class="header-anchor" href="#_2-1-对抗角色" aria-hidden="true">#</a> 2.1 对抗角色</h4>
<p>竭尽所能的捍卫自身的目标，如果无法完成任务就要勇于说“不”，也要勇于对抗，不能为达目的不择手段。“为什么”远不如“事实”重要。为什么是一个细节问题。不能因为各种原因而去省略重要的步骤</p>
<h4 id="_2-2-高风险时刻" tabindex="-1"><a class="header-anchor" href="#_2-2-高风险时刻" aria-hidden="true">#</a> 2.2 高风险时刻</h4>
<p>最要说“不”的时候就是那些高风险的时刻。越是关键时刻，“不”字就越具有价值</p>
<h4 id="_2-3-要有团队精神" tabindex="-1"><a class="header-anchor" href="#_2-3-要有团队精神" aria-hidden="true">#</a> 2.3 要有团队精神</h4>
<p>有团队精神的人，不会永远说是。那些事情做得到，那些事情做不到一定要分清楚，做不到的事情一定要讲出来，不能自信过头。</p>
<ol>
<li>试试看： 没有“试试看”这回事，尝试的意思就是“付出额外的精力”</li>
<li>消极对抗：一辆火车冲向大家，只有你一个人有所察觉，你可以选择自己轻轻抽身到马路边，眼睁睁看着其他人被车碾过，也可以选择大喊：“车来了，快离开”</li>
</ol>
<h4 id="_2-4-说-是-的成本" tabindex="-1"><a class="header-anchor" href="#_2-4-说-是-的成本" aria-hidden="true">#</a> 2.4 说“是”的成本</h4>
<p>大多数情况下，我们都希望说是。一个故事：部门招标1个月时间做一个系统，然后a公司中标了。b成员负责项目的开发，时间特别赶b成员同意做此项目。他没日没夜的加班，全部用硬编码方式完成了这个项目，自己还沾沾自喜的认为干了一件特别了不起的事情（项目开发期间还加了一些其他的功能）。结果之后部门来了个新领导砍掉了这个项目。</p>
<h4 id="_2-5-如何写出好代码" tabindex="-1"><a class="header-anchor" href="#_2-5-如何写出好代码" aria-hidden="true">#</a> 2.5 如何写出好代码</h4>
<p>有可能写出好的代码吗？有可能坚守专业主义的精神吗？在项目特别赶的情况下就可以硬编码，不管设计原则吗？其实想要写出好的代码就需要学会如何说“不”。</p>
<h3 id="第三章-说-是" tabindex="-1"><a class="header-anchor" href="#第三章-说-是" aria-hidden="true">#</a> 第三章 说“是”</h3>
<h4 id="_3-1-承诺用语" tabindex="-1"><a class="header-anchor" href="#_3-1-承诺用语" aria-hidden="true">#</a> 3.1 承诺用语</h4>
<p>做出承诺的步骤</p>
<ol>
<li>口头上说</li>
<li>心里认真对待做出的承诺</li>
<li>真正付诸行动</li>
</ol>
<h5 id="_3-1-1-识别-缺乏承诺-的征兆" tabindex="-1"><a class="header-anchor" href="#_3-1-1-识别-缺乏承诺-的征兆" aria-hidden="true">#</a> 3.1.1 识别“缺乏承诺”的征兆</h5>
<ol>
<li>需要/应当：我需要减肥、有人应当负责某某事情</li>
<li>希望/但愿：希望明天能完成任务、但愿明天有时间</li>
<li>让我们：让我们把事情做完</li>
</ol>
<h5 id="_3-1-2-真正的承诺是怎样的" tabindex="-1"><a class="header-anchor" href="#_3-1-2-真正的承诺是怎样的" aria-hidden="true">#</a> 3.1.2 真正的承诺是怎样的</h5>
<p>我将在...之前...完成任务。</p>
<ol>
<li>之所以没有成功，是因为我寄需求于某某去做这件事：比如这件事依赖于其他团队，这种情况应该提前就采取行动，提前预估风险</li>
<li>之所以没有成功，是因为我不太确信是否真正的能够完成：即使目标无法完成，你任然要不留余力的前进，离目标要更近一些</li>
<li>之所以没有成功，是因为有些时候我真的无能为力：遇到这种情况的时候要及时向承诺对象发出预警<strong>越快越好</strong>！</li>
</ol>
<h4 id="_3-2-学习如何说-是" tabindex="-1"><a class="header-anchor" href="#_3-2-学习如何说-是" aria-hidden="true">#</a> 3.2 学习如何说“是”</h4>
<ol>
<li>“试试”的另一面：试试意味着“仍然有余力可施”</li>
<li>坚守原则：彻底不要出现“如果...或许...”，也不要冒险放弃原则，比如不写测试用例等。专业人士对自己的能力极限了如指掌，清楚的知道如果保持效率的加班能持续多久，也明白要付出的代价</li>
</ol>
<p>专人人士不需要对所有请求都回答“是”。不过，他们应该努力寻找创新的方法，尽可能做到有求必应。如果给出了肯定的回答，就要使用正式的承诺。</p>
<h3 id="第四章-编码" tabindex="-1"><a class="header-anchor" href="#第四章-编码" aria-hidden="true">#</a> 第四章 编码</h3>
<h4 id="_4-1-做好准备" tabindex="-1"><a class="header-anchor" href="#_4-1-做好准备" aria-hidden="true">#</a> 4.1 做好准备</h4>
<ol>
<li>代码必须能够正常工作</li>
<li>代码必须能够帮助你解决客户提出的问题</li>
<li>代码必须和现有的系统结合的天衣无缝</li>
<li>其他程序员必须能看懂你写的代码</li>
</ol>
<ul>
<li>凌晨3点写出的代码：疲劳的时候千万不要写代码，要确保自己已经将睡眠、健康和生活方式调整到最好的状况</li>
<li>焦虑时写下的代码：焦虑的时候建议可以花点时间让自己安静下来，不要硬逼自己写代码，不然很容易写出以后不得不抛弃的代码</li>
</ul>
<h4 id="_4-2-流态区" tabindex="-1"><a class="header-anchor" href="#_4-2-流态区" aria-hidden="true">#</a> 4.2 流态区</h4>
<p>高效率状态会出现“绝无错误”的误区，这种状态会为了追求所谓速度，理性思考的能力会下降。建议可以结对编程</p>
<ul>
<li>音乐：听音乐并没有帮助“我”专注编码，反而会消耗一部分脑力资源</li>
<li>中断：中断无法避免，发生这种情况的时候要想一下，自己也可能会去打扰别人，乐于助人的态度就是专注的态度。</li>
</ul>
<h4 id="_4-3-阻塞" tabindex="-1"><a class="header-anchor" href="#_4-3-阻塞" aria-hidden="true">#</a> 4.3 阻塞</h4>
<p>无法写出代码的时候建议结对编程会有意想不到的收获</p>
<ul>
<li>创造性输入：广泛阅读各种资料来激励自己去创造</li>
</ul>
<h4 id="_4-4-调试" tabindex="-1"><a class="header-anchor" href="#_4-4-调试" aria-hidden="true">#</a> 4.4 调试</h4>
<p>真正调试的时候往往是大于编码的时间的，建议使用“测试驱动开发”可以有效的减少调试时间。医生不喜欢重新打开病人的胸腔去修复此前犯下的错误、律师不喜欢之前搞砸过的案子、写代码如果写的更多的是bug也是不专业的</p>
<h4 id="_4-5-保持节奏" tabindex="-1"><a class="header-anchor" href="#_4-5-保持节奏" aria-hidden="true">#</a> 4.5 保持节奏</h4>
<p>知道何时应该离开一会： 当遇到问题感到疲倦的时候可以离开一会，调整一下精力后才会更好。</p>
<h4 id="_4-6-进度延迟" tabindex="-1"><a class="header-anchor" href="#_4-6-进度延迟" aria-hidden="true">#</a> 4.6 进度延迟</h4>
<p>管理延迟的诀窍： 早期检测和保持透明</p>
<ol>
<li>期望：如果计划的是12天开发周期，那么不要期望自己能10天完成，否则期望会导致大麻烦。不要让其他任何人对此抱有期望</li>
<li>盲目冲刺：快速冲刺是做不到的，要明确的告诉老板，让他们不要有这种期望</li>
<li>加班加点：如果没有加班失败的后备方案，建议不要同意接受加班方案</li>
<li>交付失误：明知道没有完成任务却说完成了任务</li>
<li>定义完成：要说清楚完成的情况，是代码编写完成了还是自测完成，还是对接完成了</li>
</ol>
<h4 id="_4-7-帮助" tabindex="-1"><a class="header-anchor" href="#_4-7-帮助" aria-hidden="true">#</a> 4.7 帮助</h4>
<ol>
<li>帮助他人： 互相帮助是每个程序员的职责所在，要以能帮助他人为荣</li>
<li>接受别人的帮助：如果有人向你伸出援手，要诚挚接受，心怀感激的接受帮助并诚意合作。不要因为自己压力大而推开伸来的援手。</li>
<li>辅导：向深资导师寻求辅导也是程序员的专业职责</li>
</ol>
<h3 id="第五章-测试驱动开发" tabindex="-1"><a class="header-anchor" href="#第五章-测试驱动开发" aria-hidden="true">#</a> 第五章 测试驱动开发</h3>
<h4 id="_5-1-此事已有定论" tabindex="-1"><a class="header-anchor" href="#_5-1-此事已有定论" aria-hidden="true">#</a> 5.1 此事已有定论</h4>
<p>TDD不仅仅是一种用于缩短编码周期的简单技巧。结论很清楚，每个开发人员都需要掌握TDD</p>
<h4 id="_5-2-tdd的三项法则" tabindex="-1"><a class="header-anchor" href="#_5-2-tdd的三项法则" aria-hidden="true">#</a> 5.2 TDD的三项法则</h4>
<ol>
<li>在写好失败的单元测试之前，不要写任何产品代码</li>
<li>只要有一个单元测试失败了就不要写测试代码了，无法通过编译的也是失败的情况</li>
<li>产品代码恰好能够让当前失败的单元测试代码通过即可，无需多写</li>
</ol>
<h4 id="_5-3-tdd的优势" tabindex="-1"><a class="header-anchor" href="#_5-3-tdd的优势" aria-hidden="true">#</a> 5.3 TDD的优势</h4>
<ol>
<li>确定性： 代码有任何修改，都需要运行全部测试</li>
<li>缺陷注入率：有不少研究称TDD能显著降低缺陷</li>
<li>勇气：看到有坏味道的代码就可以马上修改，是TDD给你的勇气</li>
<li>文档：单元测试就是文档，每个单元测试都是一个调用示例</li>
<li>设计：遵循三项法则能够产生一种驱动力，促使你做出松耦合的设计</li>
</ol>
<h4 id="_5-4-局限" tabindex="-1"><a class="header-anchor" href="#_5-4-局限" aria-hidden="true">#</a> 5.4 局限</h4>
<p>在某些情况下的三项法则是不切实际的，这种情况很少，如果弊大于利就不要使用它</p>
<h3 id="第六章-练习" tabindex="-1"><a class="header-anchor" href="#第六章-练习" aria-hidden="true">#</a> 第六章 练习</h3>
<h4 id="_6-1-引子" tabindex="-1"><a class="header-anchor" href="#_6-1-引子" aria-hidden="true">#</a> 6.1 引子</h4>
<p>没门编程语言都它的hello world。这也是第一个练习，练习能够带来协调的开发节奏。无论是搏斗还是编程，速度都来源于练习</p>
<h4 id="_6-2-编程柔道场" tabindex="-1"><a class="header-anchor" href="#_6-2-编程柔道场" aria-hidden="true">#</a> 6.2 编程柔道场</h4>
<ol>
