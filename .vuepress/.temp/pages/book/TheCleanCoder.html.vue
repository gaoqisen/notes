<h4 id="_6-3-自身经验拓展" tabindex="-1"><a class="header-anchor" href="#_6-3-自身经验拓展" aria-hidden="true">#</a> 6.3 自身经验拓展</h4>
<ol>
<li>开源：可以在开源网址上去贡献代码</li>
<li>练习的职业道德：不要在上班的时候进行练习，老板的职责不包括避免你的技术落伍</li>
</ol>
<p>无论何时，专业人士都需要练习。</p>
<h3 id="第七章-验收测试" tabindex="-1"><a class="header-anchor" href="#第七章-验收测试" aria-hidden="true">#</a> 第七章 验收测试</h3>
<h4 id="_7-1-需求的沟通" tabindex="-1"><a class="header-anchor" href="#_7-1-需求的沟通" aria-hidden="true">#</a> 7.1 需求的沟通</h4>
<ol>
<li>过早精细化：经常愿意花大代价追求这种不现实的精确性
<ul>
<li>不确定原则：需求完成的越精细，就越容易被忽视。业务想看到和开发看到的不一致</li>
<li>预估焦虑：需求是一定会变化的，追求精确性是徒劳的</li>
</ul>
</li>
<li>迟来的模糊性：需求的不确定性会让开发和测试都搞错需求</li>
</ol>
<h4 id="_7-2-验收测试" tabindex="-1"><a class="header-anchor" href="#_7-2-验收测试" aria-hidden="true">#</a> 7.2 验收测试</h4>
<ol>
<li>完成的定义：不同的团队的完成的定义是不相同的</li>
<li>沟通：验收测试的目的是沟通、澄清、精确化。开发方、业务方、测试方都要达成共识</li>
<li>自动化：验收测试都应该自动进行，手动执行的都要考虑成本</li>
<li>额外工作：大量的测试用例不是额外的工作，只有确定了细节指标才可以避免开发误入岐途</li>
<li>验收测试什么时候写，由谁写：通常会交给业务分析人员、QA或者开发人员。如果是开发人员尽量不让写代码的人和开发的人是同一个人</li>
<li>开发人员的角色：开发人员有责任把验收测试与系统联系起来，让测试通过</li>
<li>测试的协商与被动推进：测试也是普通人，也会犯错误。如果发现测试用例有问题要及时沟通改进</li>
<li>验收测试和单元测试：验收测试是业务方写给业务方的。单元测试是程序员写给程序员的。</li>
<li>图形界面及其他复杂因素：尽可能减少GUI测试，GUI测试越多后期维护的难度就越大</li>
<li>持续集成：持续集成不应该失败，如果失败了团队所有人应该都要停止干活，先让测试先通过</li>
</ol>
<h3 id="第八章-测试策略" tabindex="-1"><a class="header-anchor" href="#第八章-测试策略" aria-hidden="true">#</a> 第八章 测试策略</h3>
<h4 id="_8-1-qa应该找不到任何错误" tabindex="-1"><a class="header-anchor" href="#_8-1-qa应该找不到任何错误" aria-hidden="true">#</a> 8.1 QA应该找不到任何错误</h4>
<p>开发小组应该把【QA找不到任何错误】当成努力的目标</p>
<ol>
<li>QA也是团队的一部分：他是团队中需求定义者和特性描述者</li>
<li>需求规约定义者：编写针对极端情况、边界状态、异常路径的测试</li>
</ol>
<h4 id="_8-2-自动化测试金字塔" tabindex="-1"><a class="header-anchor" href="#_8-2-自动化测试金字塔" aria-hidden="true">#</a> 8.2 自动化测试金字塔</h4>
<p>人工探索式测试 &gt; 系统测试 &gt; 集成测试 &gt; 组件测试 &gt; 单元测试</p>
<ol>
<li>单元测试：程序员自己编写的测试，应该要接近100%的覆盖率，通常要达到90%以上</li>
<li>组件测试： 针对系统的各个组件编写的，封装了业务规则（QA 和业务人员编写）</li>
<li>集成测试：对组件很多的大型系统才有意义</li>
<li>系统测试： 对整个集成完毕的系统进行的自动化测试包含吞吐率和性能测试等</li>
<li>人工探索式测试： 确保在人工操作下表现良好，同时有创造性的尽可能多的找到“古怪之处”</li>
</ol>
<h3 id="第九章-时间管理" tabindex="-1"><a class="header-anchor" href="#第九章-时间管理" aria-hidden="true">#</a> 第九章 时间管理</h3>
<h4 id="_9-1-会议" tabindex="-1"><a class="header-anchor" href="#_9-1-会议" aria-hidden="true">#</a> 9.1 会议</h4>
<p>公认：会议是必须的、会议浪费了大量的时间</p>
<ol>
<li>拒绝： 理智的使用时间，谨慎的选择需要参加的会议，礼貌的拒绝一些不必要的会议</li>
<li>离席：如果你明白继续待在会议室会浪费时间可以找合适的机会商量如何离席</li>
<li>确认议程与目标：务必弄清楚议题是什么，花多长时间，取得什么成果</li>
<li>立会：每个人回答不超过1分钟（昨天干了什么，今天打算干什么，遇到了什么问题）</li>
<li>迭代计划会议：会议的节奏要快，每个任务应该限制在5-10分钟之内，如果不够应该另选时间专人专门讨论</li>
<li>迭代回顾和demo展示： 看最新的工作成果的demo可以在最后一周的最后一天下班前45分钟召开，前20分钟回顾，后25分钟展示</li>
<li>争论/反对：如果观点无法在5-30分钟之内达成一致，就永远无法达成一致，出路是用数据说话</li>
</ol>
<h4 id="_9-2-注意力点数" tabindex="-1"><a class="header-anchor" href="#_9-2-注意力点数" aria-hidden="true">#</a> 9.2 注意力点数</h4>
<p>注意力是稀缺的资源如果注意力用光了需要1个小时或更多的时间去补充，可以在注意力不够的时候做其他事情。</p>
<ol>
<li>睡眠：充足的睡眠可以保证好的注意力</li>
<li>咖啡因：适当的咖啡可以保持注意力</li>
<li>恢复： 注意力耗尽可以自己沉思一会，或者小睡一会</li>
<li>肌肉注意力：转移注意力到肌肉注意力上面可以提升心智注意力</li>
</ol>
<h4 id="_9-3-时间拆分和番茄工作法" tabindex="-1"><a class="header-anchor" href="#_9-3-时间拆分和番茄工作法" aria-hidden="true">#</a> 9.3 时间拆分和番茄工作法</h4>
<p>给自己设定25分钟，25分钟之内无论什么干扰都需要在25分钟之后在处理，没4个番茄时间段之后休息30分钟</p>
<h4 id="_9-4-要避免的行为" tabindex="-1"><a class="header-anchor" href="#_9-4-要避免的行为" aria-hidden="true">#</a> 9.4 要避免的行为</h4>
<p>专业开发人员会评估每个任务的优先级，排除个人的喜好和需要，按照正式的紧急程度来执行任务</p>
<h4 id="_9-5-死胡同" tabindex="-1"><a class="header-anchor" href="#_9-5-死胡同" aria-hidden="true">#</a> 9.5 死胡同</h4>
<p>比如选择了走不通的技术道路越是坚持浪费的时间就越多，专业的开发人员会保持开放的头脑来听取其他意见 ，即使走到尽头，他们仍然有其他选择</p>
<h4 id="_9-6-泥潭" tabindex="-1"><a class="header-anchor" href="#_9-6-泥潭" aria-hidden="true">#</a> 9.6 泥潭</h4>
<p>专业人员时刻留意显露出来的泥潭，然后各种努力，尽快脱身。在泥潭中继续前进时不易察觉的，要及时修正设计。</p>
<h3 id="第十章-预估" tabindex="-1"><a class="header-anchor" href="#第十章-预估" aria-hidden="true">#</a> 第十章 预估</h3>
<h4 id="_10-1-什么是预估" tabindex="-1"><a class="header-anchor" href="#_10-1-什么是预估" aria-hidden="true">#</a> 10.1 什么是预估</h4>
<p>开发认为预估是猜测，业务认为预估是承诺</p>
<ol>
<li>承诺：如果你承诺某事，就必须按时完成，不兑现承诺就是一种欺骗</li>
<li>预估：它不包含任何承诺</li>
<li>暗示性承诺：试试看就是暗示性承诺，尽量不要使用</li>
</ol>
<h4 id="_10-2-pert" tabindex="-1"><a class="header-anchor" href="#_10-2-pert" aria-hidden="true">#</a> 10.2 PERT</h4>
<p>一种避免乐观的项目估计的合理方法</p>
<h4 id="_10-3-预估任务" tabindex="-1"><a class="header-anchor" href="#_10-3-预估任务" aria-hidden="true">#</a> 10.3 预估任务</h4>
<p>一组人集合起来重复进行讨论-预估的过程，直到意见统一</p>
<ol>
<li>亮手指： 统计大家树起的手指进行精细预估</li>
<li>规划扑克：用成员的扑克点数进行预估</li>
<li>关联预估：任务写在卡片上按照规则进行预估</li>
<li>三元预估： 用三种预估得出概率分布的一种方法</li>
</ol>
<h4 id="_10-4-大数定律" tabindex="-1"><a class="header-anchor" href="#_10-4-大数定律" aria-hidden="true">#</a> 10.4 大数定律</h4>
<p>把大任务分成多个小任务，分开预估在加总，结果会比单独评估大任务要准确很多</p>
<h3 id="第十一章-压力" tabindex="-1"><a class="header-anchor" href="#第十一章-压力" aria-hidden="true">#</a> 第十一章 压力</h3>
<h4 id="_11-1-避免压力" tabindex="-1"><a class="header-anchor" href="#_11-1-避免压力" aria-hidden="true">#</a> 11.1 避免压力</h4>
<ol>
<li>承诺：如果没有兑现承诺导致业务失败的压力</li>
<li>保持整洁：代码设计的整洁可以避免压力，混乱会降低速度，导致工期延误、承诺失信</li>
<li>危机中的纪律：当困境降临时，不要改好的行为。如：赶工期的时候要保证代码质量</li>
</ol>
<h4 id="_11-2-应对压力" tabindex="-1"><a class="header-anchor" href="#_11-2-应对压力" aria-hidden="true">#</a> 11.2 应对压力</h4>
<ol>
<li>不要惊慌失措：放松下来，多想一下问题，努力寻找就会带来好的结果</li>
<li>沟通：请求团队其他成员或者主管，避免产生惊恐</li>
<li>依靠你的纪律原则：战胜压力的方式就是依靠你已经知道的切实有效的东西-纪律</li>
<li>需求帮助：结对编程可以制止你的精神错乱</li>
</ol>
